<?php

namespace App\Models;

use App\Core\DB;

class Category extends DB
{
    private ?int $id = null;
    protected string $label;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function setId(?int $id): void
    {
        $this->id = $id;
    }

    public function getLabel(): string
    {
        return $this->label;
    }

    public function setLabel(string $label): void
    {
        $this->label = $label;
    }
}